"""

+   -----> Toplama
-   -----> Çıkarma
*   -----> Çarpma
/   -----> Bölme
**  -----> Kuvvet(üslü değer almak için)
//  -----> Taban Bölme(Sonucu integer olarak alır.Tam Sayı)
%   -----> Mod Alma

"""
x=12
print("X: ",x)
y=5
print("Y: ",y)
print("Toplama: ",x+y)
print("Çıkarma: ",x-y)
print("Çarpma: ",x*y)
print("Bölme: ",x/y)
print("Üslü Sayı: ",x**y)
print("Taban Bölme: ",x//y)
print("Mod Alma: ",x%y)
